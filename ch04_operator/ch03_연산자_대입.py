# 대입연산자 (=, +=, -=, *=, /=, %=, **=, //=)
a = 10
a += 3 # 더한값 대입 a = a + 3
print(a)

a = 10
a -= 3 # 뺀값 대입 a = a - 3
print(a)

a = 10
a *= 3 # 곱한값 대입 a = a * 3
print(a)

a = 10
a /= 3 # 나눈값 대입 a = a / 3
print(a)

a = 10
a %= 3 # 나머지값 대입 a = a % 3
print(a)

a = 10
a **= 3 # 제곱값 대입 a = a ** 3
print(a)

a = 10
a //= 3 # 몫값 대입 a = a // 3
print(a)